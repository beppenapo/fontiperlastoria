 <h1>Le fonti per la storia. Per un archivio delle fonti sulle valli di Primiero e Vanoi</h1>
 <p>Il progetto "Le fonti per la storia. Per un archivio delle fonti sulle valli di Primiero e Vanoi" è uno <b>strumento di ricerca</b> che mette a disposizione di studiosi ed appassionati un ampio catalogo di fonti storico-territoriali sulle valli di Primiero e Vanoi.</p>
 <p>Esso è stato ideato da un gruppo di ricercatori di Primiero su sollecitazione dei dibattiti sul senso della <b>storia territoriale</b> e sulle modalità di studio e divulgazione della stessa che, nell'estate 2008, erano sorti in seno all'allora costituenda Rete della Storia e della Memoria della Comunità di Primiero.</p>
 <br>
 <p>Questo sito è nato per rendere il più possibile fruibili i prodotti della ricerca, ossia il <big><a href="webgisPage.php" title="Link alla pagina dedicata al database">database</a></big> ed il <big><a href="webgisPage.php" title="Link alla mappa">webgis</a></big>, per il corretto utilizzo dei quali si rimanda alla sezione <a href="istruzioni.php" title="Link alla pagina delle istruzioni">istruzioni</a>.</p>
 <p>La sezione <a href="progetto.php" title="Link alla pagina del progetto">progetto</a> descrive dettagliatamente l'architettura della ricerca, i suoi scopi e le sue premesse. Le pagine dedicate alle <a href="fonti.php" title="Link alla pagina delle fonti storiche territoriali">fonti storico-territoriali</a> definiscono la natura della fonti prese in considerazione ed i criteri di individuazione e schedatura. Ulteriori approfondimenti tecnici sono ospitati nella pagina <a href="webgisPage.php" title="Link alla pagina degli strumenti">strumenti</a>.</p>
 <p>Il progetto è coordinato dalla <b>Fondazione Museo Storico del Trentino</b> nell'ambito della <b>Rete della Storia e della Memoria</b> della Comunità di Primiero.</p> 
 <br>
 <br>
 <p>Buona navigazione!</p>
