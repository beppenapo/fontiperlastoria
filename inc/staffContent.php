    <span class="aCosner">Alberto Cosner</span> (Feltre 1979) – curatore delle sezioni "fonti archeologiche" e "fonti architettoniche" e coordinatore generale del progetto.<br/>
     Laureando in Metodologia della ricerca archeologica, corso di laurea in Conservazione dei Beni Culturali, Università di Udine, dal 2000 al 2006 ha lavorato in ambito archeologico come operaio specializzato, e dal 2007 al 2010 nel settore del restauro conservativo di affreschi ed intonaci antichi. Attualmente si dedica, come libero professionista, a ricerche storico-archeologiche ed alla realizzazione di rilievi grafici finalizzati alla documentazione degli interventi restaurativi di beni storico-artistici sul territorio trentino. Dal 2011 ha in corso una collaborazione con il Parco Naturale Paneveggio Pale di San Martino nell’ambito del progetto di scavo e ricerca archeologica <i>ReMo: Il Relitto MasO. L'edificato, il vivente, il sepolto. Stratigrafie di architetture, vegetazione, suoli, attraverso l'indagine archeologica di un micro sistema insediativo montano</i>, assieme a Simone Gaio.
