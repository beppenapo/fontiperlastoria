    Laureata in Storia dell'Arte paleocristiana e medioevale all'Università Ca' Foscari di Venezia, si è addottorata nel 2009 presso l'Università degli Studi di Torino (Dottorato di ricerca in "Istituzioni, società, religioni dal Tardo Antico alla fine del Medioevo") con una tesi dal titolo: <i>Il concilio Quinisesto (692 d. C.): recezione nelle fonti occidentali (VII-X sec.)</i>. Dal 2008 è assegnista di ricerca della Fondazione "Studium Generale Marcianum" di Venezia, per il progetto “Bibbia e Cristologia nei cicli pittorici di Jacopo Tintoretto nella Scuola Grande di San Rocco”, dove è anche referente del Centro di formazione continua per l’ambito dei beni culturali. É inoltre docente a contratto  di Storia generale dell'Arte, Agiografia e agiologia, Storia e arte delle devozioni popolari presso l'Istituto Superiore di Scienze Religiose “S. Lorenzo Giustiniani” di Venezia, facente parte del sistema a rete della Facoltà Teologica del Triveneto.
